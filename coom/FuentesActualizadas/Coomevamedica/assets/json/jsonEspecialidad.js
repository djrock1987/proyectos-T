function crearAutoComp(cls) {
    $.widget("ui.combobox", {
        _create: function () {
            this.wrapper = $("<span>")
                .addClass("ui-combobox")
                .insertAfter(this.element);

            this.element.hide();
            this._createAutocomplete();
            this._createShowAllButton();
        },

        _createAutocomplete: function () {
            var selected = this.element.children(":selected"),
                value = selected.val() ? selected.text() : "";
            this.input = $("<input>")
                .appendTo(this.wrapper)
                .val(value)
                .attr("title", "")
                .attr("placeholder","Ingrese su especialidad")
                .attr("autocomplete", "on")
                .attr("style","background:#FFF")
                .addClass("ui-combobox-input ui-widget ui-widget-content ui-state-default input-valor")
                .autocomplete({
                    delay: 0,
                    minLength: 3,
                    source: $.proxy(this, "_source")
                })
                .tooltip({
                    show: {
                        effect: "slideDown",
                        delay: 250
                    },
                    position: {
                        my: "left center",
                        at: "right center",
                        collision: "flip flip"
                        //using: function( position, feedback ) {
                        //  $( this ).css( position );
                        //  $( "<div>" )
                        //    .addClass( "arrow" )
                        //    .addClass( feedback.vertical )
                        //    .addClass( feedback.horizontal )
                        //    .appendTo( this );
                        //  }
                    }
                });

            this._on(this.input, {
                autocompleteselect: function (event, ui) {
                    ui.item.option.selected = true;
                    this._trigger("select", event, {
                        item: ui.item.option
                    });
                    Titulo = ui.item.option.title;
                    cargaListaValor();
                },

                autocompletechange: "_removeIfInvalid"
            });
        },

        _createShowAllButton: function () {
            var input = this.input,
                wasOpen = false;

            $("<a>")
                .attr("tabIndex", -1)
                .attr("title", "Todos")
                .tooltip()
                .appendTo(this.wrapper)
                .button({
                    icons: {
                        primary: "ui-icon-triangle-1-s"
                    },
                    text: false
                })
                .removeClass("ui-corner-all")
                .addClass("ui-combobox-toggle ui-corner-right")
                .on("mousedown", function () {
                    wasOpen = input.autocomplete("widget").is(":visible");
                })
                .on("click", function () {
                    input.trigger("focus");

                    // Close if already visible
                    if (wasOpen) {
                        return;
                    }

                    // Pass empty string as value to search for, displaying all results
                    input.autocomplete("search", "");
                });
        },

        _source: function (request, response) {
            var matcher = new RegExp($.ui.autocomplete.escapeRegex(request.term), "i");
            response(this.element.children("option").map(function () {
                var text = $(this).text();
                var tool = $(this).attr('title')
                if (this.value && (!request.term || matcher.test(text)))
                    return {
                        label: text,
                        value: text,
                        title: tool,
                        option: this
                    };
            }));
        },

        _removeIfInvalid: function (event, ui) {
            // Selected an item, nothing to do
            if (ui.item) {
                this.input.attr("title", ui.item == null ? "" : ui.item.option.title);
                return;
            }

            // Search for a match (case-insensitive)
            var value = this.input.val(),
                valueLowerCase = value.toLowerCase(),
                valid = false;
            this.element.children("option").each(function () {
                if ($(this).text().toLowerCase() === valueLowerCase) {
                    this.selected = valid = true;
                    return false;
                }
            });

            // Found a match, nothing to do
            if (valid) {
                this.input.attr("title", ui.item == null ? "" : ui.item.option.title);
                return;
            }

            // Remove invalid value
            this.input
                .val("")
                .attr("title", value + " Sin coincidencias")
                .tooltip("Todos");
            this.element.val("");
            this._delay(function () {
                this.input.tooltip("close").attr("title", "");
            }, 2500);
            this.input.autocomplete("instance").term = "";
        },

        _destroy: function () {
            this.wrapper.remove();          
            this.element.show();
            //$.Widget.prototype.destroy.call(this);
        },
    });
    $('.' + cls + '').combobox();
};

function destruirAutoCom(cls) {
    $('.' + cls + '').combobox('destroy');
}